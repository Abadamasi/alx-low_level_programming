#include "3-calc.h"

/**
 * op_add - runs a addition calculation on a and b;
 * @a: int value.
 * @b: int value.
 *
 * Return: Resault of math operation.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - runs a subtraction calculation on a and b;
 * @a: int value.
 * @b: int value.
 *
 * Return: Resault of math operation.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

